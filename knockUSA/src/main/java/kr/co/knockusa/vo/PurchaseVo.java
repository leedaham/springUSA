package kr.co.knockusa.vo;

public class PurchaseVo {
	private String purchase_no;
	private int purchase_goods_no;
	private String purchase_id;
	private int purchase_amount;
	private int purchase_price;
	private int purchase_goods_status;
	private String purchase_rdate;
	
	public String getPurchase_no() {
		return purchase_no;
	}
	public void setPurchase_no(String purchase_no) {
		this.purchase_no = purchase_no;
	}
	public int getPurchase_goods() {
		return purchase_goods_no;
	}
	public void setPurchase_goods(int purchase_goods_no) {
		this.purchase_goods_no = purchase_goods_no;
	}
	public String getPurchase_id() {
		return purchase_id;
	}
	public void setPurchase_id(String purchase_id) {
		this.purchase_id = purchase_id;
	}
	public int getPurchase_amount() {
		return purchase_amount;
	}
	public void setPurchase_amount(int purchase_amount) {
		this.purchase_amount = purchase_amount;
	}
	public int getPurchase_price() {
		return purchase_price;
	}
	public void setPurchase_price(int purchase_price) {
		this.purchase_price = purchase_price;
	}
	public int getPurchase_goods_status() {
		return purchase_goods_status;
	}
	public void setPurchase_goods_status(int purchase_goods_status) {
		this.purchase_goods_status = purchase_goods_status;
	}
	public String getPurchase_rdate() {
		return purchase_rdate;
	}
	public void setPurchase_rdate(String purchase_rdate) {
		this.purchase_rdate = purchase_rdate;
	}
	
}
